int getNode(SinglyLinkedListNode* head, int positionFromTail) {

    SinglyLinkedListNode *t = head, *r = head;
    int val = 0, count = 0;
    while(count <= positionFromTail) {
        r = r->next;
        count += 1;
    }
    while(r) {
        r = r->next;
        t = t->next;
    }
    val = t->data;

    return val;
}

